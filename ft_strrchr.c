char	*ft_strrchr(const char *s, int c)
{
	while (*s != 0)
		s++;
	if (*s == c)
		return ((char *)s);
	s--;
	while (*s != 0)
	{
		if (*s == c)
			return ((char *)s);
		s--;
	}
	return (0);
}
